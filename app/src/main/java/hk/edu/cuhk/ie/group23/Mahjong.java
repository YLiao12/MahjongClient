package hk.edu.cuhk.ie.group23;

public class Mahjong {
    private int id;
    private String name;

    public Mahjong(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
